public class AddIvo {
	int a, b;
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int  AddIvo() {
		
		return (a+b)-(b-a)/(a+b)-(a-b);
	}

}
